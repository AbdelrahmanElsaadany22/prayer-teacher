export const PRAYER_NAMES_AR: Record<string, string> = {
  Fajr: 'الفجر',
  Zuhr: 'الظهر',
  Dhuhr: 'الظهر',
  Asr: 'العصر',
  Maghrib: 'المغرب',
  Isha: 'العشاء',
};

export function localizePrayerName(name: string, lang: Lang): string {
  if (lang === 'ar') return PRAYER_NAMES_AR[name] ?? name;
  return name;
}

